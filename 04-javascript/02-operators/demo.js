// 02 · 运算符 demo
// 聚焦：算术 / 比较 / 逻辑 / 赋值 / 位 / 三元 / 可选链 ?. / 空值合并 ??
//       == vs ===、隐式类型转换
// 浏览器和 node 均可运行

'use strict';

const lines = [];
function log(...args) {
  const text = args
    .map((a) => (typeof a === 'object' && a !== null ? JSON.stringify(a) : String(a)))
    .join(' ');
  lines.push(text);
  console.log(text);
}

// ============================================================
// 一、算术运算符
// ============================================================
log('===== 一、算术运算符 =====');
log('加 7 + 3 =', 7 + 3);
log('减 7 - 3 =', 7 - 3);
log('乘 7 * 3 =', 7 * 3);
log('除 7 / 3 =', 7 / 3);
log('取余 7 % 3 =', 7 % 3); // 余数
log('幂 2 ** 10 =', 2 ** 10); // 次方
log('字符串拼接 "a" + 1 =', 'a' + 1); // 加号遇字符串变拼接

// ============================================================
// 二、比较运算符 + == vs ===
// ============================================================
log('\n===== 二、比较 与 == vs === =====');
// === 严格相等：不做类型转换，类型不同直接 false
log('1 === "1" =>', 1 === '1'); // false
// == 宽松相等：会先做隐式类型转换再比较
log('1 == "1"  =>', 1 == '1'); // true（字符串转数字）
log('0 == false =>', 0 == false); // true（都转成 0）
log('null == undefined =>', null == undefined); // true（特例）
log('null === undefined =>', null === undefined); // false
log('NaN == NaN =>', NaN == NaN); // false（NaN 不等于任何值）
log('大小比较 5 > 3 =>', 5 > 3, '| "b" > "a" =>', 'b' > 'a'); // 字符串按字符编码比

// ============================================================
// 三、逻辑运算符（短路求值）
// ============================================================
log('\n===== 三、逻辑运算符 =====');
log('与 true && "yes" =>', true && 'yes'); // 返回后者
log('与 false && "yes" =>', false && 'yes'); // 短路返回 false
log('或 "" || "默认值" =>', '' || '默认值'); // 前者假值，返回后者
log('或 "已有" || "默认" =>', '已有' || '默认');
log('非 !0 =>', !0, '| !"x" =>', !'x'); // 取反并转布尔

// ============================================================
// 四、赋值运算符
// ============================================================
log('\n===== 四、赋值运算符 =====');
let n = 10;
n += 5; // 等价 n = n + 5
log('n += 5 =>', n);
n *= 2;
log('n *= 2 =>', n);
let s = 'a';
s += 'bc'; // 字符串也能用
log('s += "bc" =>', s);
let z = null;
z ??= '兜底'; // 逻辑空赋值：仅当 z 为 null/undefined 才赋值
log('z ??= "兜底" =>', z);

// ============================================================
// 五、位运算符（基于二进制）
// ============================================================
log('\n===== 五、位运算符 =====');
log('与 5 & 3 =>', 5 & 3); // 0101 & 0011 = 0001 = 1
log('或 5 | 3 =>', 5 | 3); // 0101 | 0011 = 0111 = 7
log('异或 5 ^ 3 =>', 5 ^ 3); // 0101 ^ 0011 = 0110 = 6
log('左移 1 << 4 =>', 1 << 4); // 相当于 1 * 2^4 = 16
log('右移 16 >> 2 =>', 16 >> 2); // 相当于 16 / 2^2 = 4
log('判断奇偶 7 & 1 =>', 7 & 1); // 末位为 1 即奇数

// ============================================================
// 六、三元运算符
// ============================================================
log('\n===== 六、三元运算符 =====');
const age = 20;
const group = age >= 18 ? '成年人' : '未成年人'; // 条件 ? 真值 : 假值
log('age >= 18 ?', group);

// ============================================================
// 七、可选链 ?. 与 空值合并 ??
// ============================================================
log('\n===== 七、?. 与 ?? =====');
const user = { name: 'Tom', address: { city: '北京' } };
// 可选链：访问不存在的属性不报错，返回 undefined
log('user.address?.city =>', user.address?.city); // 北京
log('user.contact?.phone =>', user.contact?.phone); // undefined（不报错）
log('可选调用 user.sayHi?.() =>', user.sayHi?.()); // undefined（方法不存在也安全）

// 空值合并 ??：仅当左侧为 null/undefined 才用右侧（区别于 || 把 0、"" 也当假）
const count = 0;
log('count ?? 99 =>', count ?? 99); // 0（保留了 0）
log('count || 99 =>', count || 99); // 99（|| 把 0 当假值了）

// ============================================================
// 八、隐式类型转换小结
// ============================================================
log('\n===== 八、隐式类型转换 =====');
log('"5" - 2 =>', '5' - 2); // 3，减号触发转数字
log('"5" + 2 =>', '5' + 2); // "52"，加号触发拼接
log('true + 1 =>', true + 1); // 2，true 转 1
log('[] + [] =>', JSON.stringify([] + [])); // "" 空字符串
log('Boolean("") =>', Boolean(''), '| Boolean("0") =>', Boolean('0')); // false / true

// ============================================================
// 输出到页面
// ============================================================
if (typeof document !== 'undefined') {
  const pre = document.getElementById('output');
  if (pre) pre.textContent = lines.join('\n');
}
