/**
 * 06 · 枚举（Enums）
 * ---------------------------------------------
 * 枚举用于定义一组「命名常量」。TS 支持数字枚举、字符串枚举、常量枚举。
 * 注意：枚举是 TS 少数会「生成额外 JS 运行时代码」的特性，使用需谨慎。
 */

// ───────────────────────────────────────────────
// 1. 数字枚举（默认从 0 自增）
// ───────────────────────────────────────────────

enum Direction {
  Up, // 0
  Down, // 1
  Left, // 2
  Right, // 3
}
console.log("数字枚举:", Direction.Up, Direction.Right); // 0 3

// 也可手动指定起始值，后续自动 +1
enum StatusCode {
  Success = 200,
  NotFound = 404,
  ServerError = 500,
}

// ───────────────────────────────────────────────
// 2. 反向映射（仅数字枚举有！）
// ───────────────────────────────────────────────
// 数字枚举会同时生成 名->值 和 值->名 两套映射
console.log("反向映射:", Direction[0], StatusCode[404]); // "Up"  "NotFound"

// ❌ 错误示范：字符串枚举没有反向映射
// 见下方 Color，Color["RED"] 这种「值->名」访问是不存在的。

// ───────────────────────────────────────────────
// 3. 字符串枚举（无自增、无反向映射，但可读性更好）
// ───────────────────────────────────────────────

enum Color {
  Red = "RED",
  Green = "GREEN",
  Blue = "BLUE",
}
console.log("字符串枚举:", Color.Red); // "RED"

// ❌ 错误示范：字符串枚举每个成员都必须显式赋值
// enum BadColor { Red = "RED", Green } // Green 缺少初始值会报错
// 报错：枚举成员必须具有初始化表达式。

// ───────────────────────────────────────────────
// 4. 异构枚举（混合数字和字符串）—— 简述，不推荐
// ───────────────────────────────────────────────
enum Mixed {
  No = 0,
  Yes = "YES",
}
// 语法合法，但语义混乱，实践中几乎不用。
console.log("异构枚举:", Mixed.No, Mixed.Yes);

// ───────────────────────────────────────────────
// 5. 常量枚举 const enum（编译时内联，不生成对象）
// ───────────────────────────────────────────────

const enum Size {
  Small,
  Medium,
  Large,
}
// 使用处会被直接替换成字面量 0/1/2，编译后没有 Size 对象，体积更小、更快
const mySize = Size.Medium;
console.log("常量枚举(已内联为字面量):", mySize); // 1

// 注意：const enum 不能被反向映射，也不能在某些 isolatedModules 配置下跨文件使用。

// ───────────────────────────────────────────────
// 6. 为什么很多项目用「联合字面量」替代 enum（重要坑）
// ───────────────────────────────────────────────
// 普通 enum 会生成运行时 JS 对象（增加打包体积），且数字枚举类型较「松」。
// 联合字面量类型 = 纯类型层面，零运行时开销，且能直接被 JSON / 字符串复用。

type Fruit = "apple" | "banana" | "orange"; // 联合字面量

function eat(fruit: Fruit) {
  console.log("吃了:", fruit);
}
eat("apple");

// ❌ 错误示范：传入不在联合中的值
// eat("watermelon");
// 报错：类型“"watermelon"”的参数不能赋给类型“Fruit”的参数。

// 数字枚举的「松」体现在：任意 number 可赋值给数字枚举类型（历史遗留）
let sc: StatusCode = 999; // 不报错！这是数字枚举的坑
console.log("数字枚举可被任意 number 赋值(坑):", sc);

console.log("演示结束");
