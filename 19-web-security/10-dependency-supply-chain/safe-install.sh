#!/usr/bin/env bash
# ============================================================================
# safe-install.sh —— 前端依赖「安全安装 / 审计」流程示范（仅供学习）
#
# 本脚本是【命令 + 中文注释的清单】，用于演示如何安全地安装与审计 npm 依赖。
# ⚠️ 注意：这里只作示范，请勿在不了解的情况下直接对生产项目批量执行。
#         下面很多命令会联网（如 npm ci / npm audit），如只想阅读请勿运行。
# ============================================================================

set -euo pipefail   # 出错即停、未定义变量报错、管道任一环失败即失败（写脚本的好习惯）

# ----------------------------------------------------------------------------
# 一、用锁文件精确安装：npm ci（而不是 npm install）
# ----------------------------------------------------------------------------
# npm ci 会【严格按 package-lock.json】安装每个包的确切版本 + 校验 integrity 哈希。
# 与 npm install 的区别：
#   - 若 package.json 与 package-lock.json 对不上，npm ci 直接【报错退出】，
#     而不会像 npm install 那样悄悄改写锁文件（可能把版本升到被投毒的补丁版）。
#   - 它会先删掉旧的 node_modules，保证构建【可复现】。
# 结论：CI/CD 里一律用 npm ci，把实际安装的版本钉死在被审计过的锁文件上。
npm ci

# ----------------------------------------------------------------------------
# 二、扫描已知漏洞（CVE）：npm audit
# ----------------------------------------------------------------------------
# 拿整棵依赖树（含传递依赖）去比对漏洞数据库，列出有已知 CVE 的包及严重级别。
# 对应 OWASP A06「易受攻击和过时的组件」。
npm audit                       # 查看全部漏洞报告
npm audit --omit=dev            # 只看【生产依赖】的漏洞，过滤掉 devDependencies 噪音
npm audit --audit-level=high    # 仅当存在 high/critical 漏洞时才以非 0 退出（适合卡 CI 门禁）

# 尝试自动修复（会在 semver 兼容范围内升级有漏洞的包）：
# npm audit fix                 # 谨慎使用：可能改动锁文件
# npm audit fix --force         # ⚠️ 危险：会做破坏性大版本升级，可能弄坏构建，别在 CI 盲跑

# ----------------------------------------------------------------------------
# 三、阻断恶意生命周期脚本：--ignore-scripts
# ----------------------------------------------------------------------------
# npm install 一个包时，它的 preinstall / install / postinstall 会自动执行任意代码，
# 这是供应链投毒「安装即中招」的主要落地方式（窃取 ~/.npmrc、环境变量、SSH 密钥并外传）。
# 加 --ignore-scripts 可跳过所有生命周期脚本，从根上堵住这条路。
npm ci --ignore-scripts         # 按锁文件安装，且不执行任何依赖的生命周期脚本

# 也可以【全局默认】关闭脚本执行（之后所有安装都不跑脚本，需要时再单独放行）：
# npm config set ignore-scripts true

# 或者迁移到 pnpm：pnpm 默认【不执行】依赖的构建脚本，
# 需要构建的包要在 package.json 的 pnpm.onlyBuiltDependencies 里显式列出放行。
# pnpm install

# ----------------------------------------------------------------------------
# 四、其他有用的审计 / 治理命令
# ----------------------------------------------------------------------------
npm outdated                    # 列出有更新的依赖，提醒定期升级（别让依赖年久失修）
npm ls --all                    # 打印完整依赖树，看清到底装了哪些（传递）依赖
# npm sbom --sbom-format cyclonedx > sbom.json   # 生成 SBOM 软件物料清单，出漏洞时快速定位

# ----------------------------------------------------------------------------
# 五、防依赖混淆（Dependency Confusion）的配置要点（示范，非可执行安装）
# ----------------------------------------------------------------------------
# 给私有包统一加 scope（如 @mycorp/），并在 .npmrc 中把该 scope 绑定到私有 registry，
# 这样公共 npm 上抢注的同名高版本包就顶替不了你的私有包：
#   echo "@mycorp:registry=https://npm.mycorp.internal/" >> .npmrc

echo "✅ 安全安装 / 审计流程示范结束（仅供学习）"
